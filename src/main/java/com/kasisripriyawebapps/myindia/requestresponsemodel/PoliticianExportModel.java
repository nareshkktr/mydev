package com.kasisripriyawebapps.myindia.requestresponsemodel;

public class PoliticianExportModel {
	
	private String politicianName;
	private String partyName;
	private String locationName;
	private String designation;
	private String politicianType;
	
	public String getPoliticianName() {
		return politicianName;
	}
	public void setPoliticianName(String politicianName) {
		this.politicianName = politicianName;
	}
	public String getPartyName() {
		return partyName;
	}
	public void setPartyName(String partyName) {
		this.partyName = partyName;
	}
	public String getLocationName() {
		return locationName;
	}
	public void setLocationName(String locationName) {
		this.locationName = locationName;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getPoliticianType() {
		return politicianType;
	}
	public void setPoliticianType(String politicianType) {
		this.politicianType = politicianType;
	}

}
